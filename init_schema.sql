-- Tables
CREATE SEQUENCE IF NOT EXISTS go_campaigns_id_seq;
CREATE TABLE IF NOT EXISTS go_campaigns (
  id bigint NOT NULL DEFAULT nextval('go_campaigns_id_seq'),
  go_code text NOT NULL UNIQUE,
  category text NOT NULL CHECK (category = ANY (ARRAY['POCA_SET','ALBUM','RESTOCK'])),
  idol_name text NOT NULL,
  album text NOT NULL,
  version text,
  retail_store text,
  url text,
  set_price_krw numeric,
  retail_price_krw numeric,
  discount_pct numeric,
  pob_store text,
  pob_detail text,
  set_detail text,
  detail text,
  deadline date,
  is_closed boolean NOT NULL DEFAULT false,
  note text,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (id)
);

CREATE SEQUENCE IF NOT EXISTS go_buyer_orders_id_seq;
CREATE TABLE IF NOT EXISTS go_buyer_orders (
  id bigint NOT NULL DEFAULT nextval('go_buyer_orders_id_seq'),
  alibaba_username text NOT NULL,
  contact_email text,
  contact_note text,
  total_krw numeric NOT NULL DEFAULT 0,
  status text NOT NULL DEFAULT 'submitted' CHECK (status = ANY (ARRAY['submitted','confirmed','shipped','cancelled'])),
  notified_at timestamptz,
  created_at timestamptz NOT NULL DEFAULT now(),
  updated_at timestamptz NOT NULL DEFAULT now(),
  PRIMARY KEY (id)
);

CREATE SEQUENCE IF NOT EXISTS go_buyer_order_items_id_seq;
CREATE TABLE IF NOT EXISTS go_buyer_order_items (
  id bigint NOT NULL DEFAULT nextval('go_buyer_order_items_id_seq'),
  order_id bigint NOT NULL,
  go_code text NOT NULL,
  qty integer NOT NULL CHECK (qty > 0),
  unit_price_krw numeric,
  line_total_krw numeric,
  PRIMARY KEY (id),
  FOREIGN KEY (order_id) REFERENCES go_buyer_orders(id)
);

CREATE SEQUENCE IF NOT EXISTS cron_runs_id_seq;
