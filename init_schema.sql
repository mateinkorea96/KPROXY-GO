-- Functions
CREATE OR REPLACE FUNCTION public.go_campaign_status(p_deadline date, p_is_closed boolean)
RETURNS text LANGUAGE sql IMMUTABLE AS $$
  SELECT CASE
    WHEN p_is_closed OR p_deadline IS NULL OR p_deadline < CURRENT_DATE THEN 'closed'
    WHEN p_deadline - CURRENT_DATE <= 3 THEN 'closing_soon'
    ELSE 'open'
  END;
$$;

CREATE OR REPLACE FUNCTION public.decrement_inventory_stock(
  p_sku text, p_qty integer, p_warehouse text DEFAULT 'kr'
)
RETURNS TABLE(
  matched boolean,
  inventory_id bigint,
  before_stock integer,
  after_stock integer,
  safety_stock integer,
  needs_reorder boolean
)
LANGUAGE plpgsql AS $$
DECLARE
  v_id BIGINT;
  v_before INTEGER;
  v_safety INTEGER;
  v_after INTEGER;
BEGIN
  IF p_warehouse = 'tw' THEN
    SELECT inv.id, inv.stock_tw, inv.safety_stock_tw
      INTO v_id, v_before, v_safety
      FROM inventory inv WHERE inv.id = (SELECT id FROM inventory WHERE location||idol||album||version||member = p_sku LIMIT 1);
  ELSE
    SELECT inv.id, inv.stock_kr, inv.safety_stock
      INTO v_id, v_before, v_safety
      FROM inventory inv WHERE inv.id = (SELECT id FROM inventory WHERE location||idol||album||version||member = p_sku LIMIT 1);
  END IF;
  IF v_id IS NULL THEN
    RETURN QUERY SELECT false, NULL::BIGINT, NULL::INTEGER, NULL::INTEGER, NULL::INTEGER, false;
    RETURN;
  END IF;
  v_after := GREATEST(v_before - p_qty, 0);
  IF p_warehouse = 'tw' THEN
    UPDATE inventory SET stock_tw = v_after, updated_at = now() WHERE id = v_id;
  ELSE
    UPDATE inventory SET stock_kr = v_after, updated_at = now() WHERE id = v_id;
  END IF;
  RETURN QUERY SELECT true, v_id, v_before, v_after, v_safety, (v_after <= COALESCE(v_safety, 0));
END;
$$;

-- View
CREATE OR REPLACE VIEW public.go_campaign_aggregate AS
SELECT
  c.go_code, c.category, c.idol_name, c.album, c.version,
  c.retail_store, c.deadline,
  go_campaign_status(c.deadline, c.is_closed) AS status,
  COALESCE(sum(CASE WHEN o.status = ANY (ARRAY['submitted','confirmed']) THEN i.qty ELSE 0 END), 0) AS total_qty,
  COALESCE(count(DISTINCT CASE WHEN o.status = ANY (ARRAY['submitted','confirmed']) THEN o.id ELSE NULL END), 0) AS buyer_count,
  COALESCE(sum(CASE WHEN o.status = ANY (ARRAY['submitted','confirmed']) THEN i.line_total_krw ELSE 0 END), 0) AS total_krw
FROM go_campaigns c
LEFT JOIN go_buyer_order_items i ON i.go_code = c.go_code
LEFT JOIN go_buyer_orders o ON o.id = i.order_id
GROUP BY c.id, c.go_code, c.category, c.idol_name, c.album, c.version,
         c.retail_store, c.deadline, c.is_closed;
